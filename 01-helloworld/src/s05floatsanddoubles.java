
public class s05floatsanddoubles {

	public static void main(String[] args) {
		
		// float
         System.out.println("min float vlaue:" + Float.MIN_VALUE);
         System.out.println("max float vlaue:" + Float.MAX_VALUE);
//         double
         System.out.println("min double vlaue:" + Double.MIN_VALUE);
         System.out.println("max double vlaue:" + Double.MAX_VALUE);
	        
         
         float x=5.45f;
         System.out.println(x);
         
         float a=22/7;
         System.out.println(a);
         
         double b=22/7;
         System.out.println(b);
         
         float c=22/7f;
         System.out.println(c);
         
         double d=22/7d;
         System.out.println(d);
         double e=22d/7d;
         System.out.println(e);
	}

}
